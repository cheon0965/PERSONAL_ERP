#!/usr/bin/env node

const fs = require('node:fs');
const os = require('node:os');
const path = require('node:path');
const { spawnSync } = require('node:child_process');
const mysql = require('mysql2/promise');

const repoRoot = path.resolve(__dirname, '..');
const apiRoot = path.join(repoRoot, 'apps', 'api');
const sourceSchemaPath = path.join(apiRoot, 'prisma', 'schema.prisma');
const migrationName = '20260412150000_latest_schema_baseline';
const databaseUrl = process.env.DATABASE_URL;

main().catch((error) => {
  console.error('[latest-baseline] Failed to deploy latest schema baseline.');
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});

async function main() {
  if (!databaseUrl) {
    throw new Error('DATABASE_URL is required.');
  }

  await assertTargetDatabaseIsEmpty(databaseUrl);

  const tempRoot = fs.mkdtempSync(
    path.join(os.tmpdir(), 'personal-erp-prisma-baseline-')
  );

  try {
    const tempPrismaDir = path.join(tempRoot, 'prisma');
    const tempMigrationsDir = path.join(tempPrismaDir, 'migrations');
    const tempMigrationDir = path.join(tempMigrationsDir, migrationName);
    const tempSchemaPath = path.join(tempPrismaDir, 'schema.prisma');

    fs.mkdirSync(tempMigrationDir, { recursive: true });
    fs.copyFileSync(sourceSchemaPath, tempSchemaPath);
    fs.writeFileSync(
      path.join(tempMigrationsDir, 'migration_lock.toml'),
      '# Please do not edit this file manually\nprovider = "mysql"\n',
      'utf8'
    );

    const migrationSql = generateLatestSchemaMigrationSql();
    fs.writeFileSync(
      path.join(tempMigrationDir, 'migration.sql'),
      migrationSql,
      'utf8'
    );

    console.log(
      `[latest-baseline] Applying ${migrationName} to an empty target database.`
    );

    runPrisma(['migrate', 'deploy', '--schema', tempSchemaPath], {
      stdio: 'inherit'
    });
  } finally {
    fs.rmSync(tempRoot, { recursive: true, force: true });
  }
}

async function assertTargetDatabaseIsEmpty(url) {
  const databaseName = readDatabaseName(url);
  const connection = await mysql.createConnection(url);

  try {
    const [rows] = await connection.query('SHOW TABLES');
    const tableNames = rows
      .map((row) => Object.values(row)[0])
      .filter((name) => typeof name === 'string');

    if (tableNames.length > 0) {
      throw new Error(
        [
          `DATABASE_URL points to "${databaseName}", but it is not empty.`,
          `Existing tables: ${tableNames.join(', ')}`,
          'Use the normal migration history with `npm run db:deploy` for existing databases.'
        ].join(' ')
      );
    }

    console.log(
      `[latest-baseline] Target database "${databaseName}" is empty.`
    );
  } finally {
    await connection.end();
  }
}

function readDatabaseName(url) {
  const parsed = new URL(url);
  const databaseName = decodeURIComponent(parsed.pathname.replace(/^\//, ''));

  if (!databaseName) {
    throw new Error('DATABASE_URL must include a database name.');
  }

  return databaseName;
}

function generateLatestSchemaMigrationSql() {
  const result = runPrisma(
    [
      'migrate',
      'diff',
      '--from-empty',
      '--to-schema-datamodel',
      sourceSchemaPath,
      '--script'
    ],
    {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'pipe']
    }
  );

  return normalizeGeneratedSql(result.stdout);
}

function normalizeGeneratedSql(sql) {
  const normalized = sql.replace(/\r\n/g, '\n').trim();

  if (!normalized) {
    throw new Error('Prisma produced an empty baseline migration.');
  }

  return `${normalized}\n`;
}

function runPrisma(args, options) {
  const result = spawnSync(resolvePrismaCommand(), args, {
    cwd: apiRoot,
    env: process.env,
    ...options
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    const stderr =
      typeof result.stderr === 'string' ? result.stderr.trim() : '';
    throw new Error(
      stderr || `Prisma command failed: prisma ${args.join(' ')}`
    );
  }

  return result;
}

function resolvePrismaCommand() {
  const extension = process.platform === 'win32' ? '.cmd' : '';
  const localBin = path.join(
    repoRoot,
    'node_modules',
    '.bin',
    `prisma${extension}`
  );

  return fs.existsSync(localBin) ? localBin : 'prisma';
}
